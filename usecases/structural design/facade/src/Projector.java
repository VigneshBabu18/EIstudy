// Projector class simulates a projector component in the home theater system
public class Projector {
    
    // Method to turn on the projector
    public void on() {
        System.out.println("Projector is on");
    }

    // Method to set the projector to widescreen mode
    public void wideScreenMode() {
        System.out.println("Projector in widescreen mode");
    }

    // Method to turn off the projector
    public void off() {
        System.out.println("Projector is off");
    }
}
