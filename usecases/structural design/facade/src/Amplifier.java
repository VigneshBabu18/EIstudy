// Amplifier class simulates an amplifier component in the home theater system
public class Amplifier {
    
    // Method to turn on the amplifier
    public void on() {
        System.out.println("Amplifier is on");
    }

    // Method to set the volume of the amplifier
    public void setVolume(int level) {
        System.out.println("Amplifier volume set to " + level);
    }

    // Method to turn off the amplifier
    public void off() {
        System.out.println("Amplifier is off");
    }
}
